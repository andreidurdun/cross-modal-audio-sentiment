        {
            "name": "wavlm_audio_kd_text_en_text_fr_audio_retrained_teacher",
            "kind": "audio",
            "checkpoint_dir": PROJECT_ROOT / "checkpoints" / "wavlm_audio_kd_text_en_text_fr_audio_retrained_teacher",
            "kd_profile": True,
        },
